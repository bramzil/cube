#include "libft.h"

size_t	ft_strllen(char *s, char c)
{
	size_t			i;

	i = 0;
	if (!s)
		return (0);
	if (c == '\n' && !ft_strchr(s, c))
		return (0);
	while (*s && (*s != c) && ++i)
		s++;
	return (i + 1 * (c == '\n'));
}
